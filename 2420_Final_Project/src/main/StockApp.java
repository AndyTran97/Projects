    private String[] getCities(){
        ArrayList<String> list = new ArrayList<>();
        EdgeWeightedDigraph graph = sg.digraph();
        for(DirectedEdge e: graph.edges()){
            int v = e.from();
            int w = e.to();
            if(!list.contains(sg.nameOf(v))){
                list.add(sg.nameOf(v));
            }
            if(!list.contains(sg.nameOf(w))){
                list.add(sg.nameOf(w));
            }
        }
        cities = new String[list.size()];
              
        for(int i = 0; i < cities.length; i++){
            cities[i] = list.get(i);
        }
        
        return cities;
    } 
    
