    private void exportBtn1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_exportBtn1ActionPerformed
    {//GEN-HEADEREND:event_exportBtn1ActionPerformed
     
        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Export File");
        chooser.setFileFilter(new FileTypeFilter(".csv", "CSV File"));

        // Export the .csv file
        int result = chooser.showSaveDialog(null);
        if(result == JFileChooser.APPROVE_OPTION){
            try{
                File f = chooser.getSelectedFile();
                String fileName = f.getAbsolutePath();
                if(!fileName.contains(".csv")){
                    fileName += ".csv";
                }
                Out out = new Out(fileName);

               String head = "";
               for(int i = 0; i < tableHead.length; i++) {
                       if(i < tableHead.length - 1)
                               head = head + tableHead[i] + ",";
                       else {
                               head = head + tableHead[i] + "\n";
                       }
               }
               out.print(head);

               for(Integer k: st.keys()) {
                       String data = st.get(k).getString() + "\n";
                       out.print(data);
               }
               out.close();
               JOptionPane.showMessageDialog(null,
                                "Successfully Export file",
                                "Message",
                                JOptionPane.INFORMATION_MESSAGE);
            } catch(Exception e){
                               JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        
    }//GEN-LAST:event_exportBtn1ActionPerformed

